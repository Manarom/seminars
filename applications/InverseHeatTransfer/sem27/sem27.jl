# ╔═╡ 8f6d3be5-ffb5-41d5-8e50-f4353193c53c
begin 
	x = SVector{N}(collect(range(Tinit,Tmax,N)));
	PolyType = BernsteinSymPolyWrapper{length(lam_pars),Float64}
	V = VanderMatrix(x,PolyType)
	lam_values = Vector(V*SVector(lam_pars)) 
	plot(Vector(x),Vector(lam_values),title="thermal conductivity")
end

# ╔═╡ e4aac0a1-d9c0-4e20-8f86-151090a9651f
begin 
	lam_prs = Polynomials.fit(Polynomial{Float64},Vector(x),lam_values,3)
	lam_poly = Polynomials.ImmutablePolynomial( lam_prs )
	lam_fun = T -> lam_poly(T) # теплопроводность
	lam_der_poly = Polynomials.ImmutablePolynomial( derivative(lam_prs))
	lam_der = T->lam_der_poly(T)#;% производная теплопроводности
	
	Cp_fun = _ -> Cp*Ro# capacity
	initT_f = _ -> Tinit # starting 
	BC_dwn_f = _ -> Tinit# dwn BC
	BC_up_f = t -> Tinit + t*(Tmax - Tinit)/tmax# upper BC
end

